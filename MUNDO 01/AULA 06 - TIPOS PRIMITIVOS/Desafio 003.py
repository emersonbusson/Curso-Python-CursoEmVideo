# Desafio 003 - Crie um programa que leia dois números e mostre a soma entre eles.
print('Soma entre dois números!')
primeiro_numero = float(input('Digite o primeiro número:'))
segundo_numero = float(input('Digite o segundo número:'))

print(f'A soma entre os números {primeiro_numero} e {segundo_numero} é: {primeiro_numero + segundo_numero}.')